#include <bits/stdc++.h>
using namespace std;

int main()
{
    int t;
    scanf("%d",&t);
    int n,a,b;
    while(t--){
        scanf("%d",&n);
        pair<int,int> sheep[n];
        for(int i=0;i<n;i++){
            scanf("%d %d",&a,&b);
            sheep[i].first=a;
            sheep[i].second=b;
        }

    }
    return 0;
}
